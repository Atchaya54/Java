package Patterns;

public class Num_pattern2 {
	public static void main(String[] args) {
	    int n=4;
	    int v=0;
	    for(int i=1;i<=n+1;i++){
	        for(int j=1;j<=i;j++){
	            v++;
	           System.out.print(v+" "); 
	        }
	   System.out.println();    
	    }
	   
	}

}
